function validarFormulario() {
    var valor = true;
    if ($("#nombre_formulario").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Nombre no puede estar vac√≠o.");
        $("#nombre_formulario").focus();
    }
    return valor;
}

function limpiarFormulario() {
    $("#id_formulario").val("0");
    $("#nombre_formulario").val("");
}

